<script>
(function () {
    'use strict';

    var TOTAL    = 8;
    var DURATION = 5500;

    var slides   = document.querySelectorAll('.hs__slide');
    var thumbs   = document.querySelectorAll('.hs__thumb');
    var prevBtn  = document.getElementById('hsPrev');
    var nextBtn  = document.getElementById('hsNext');
    var fill     = document.getElementById('hsProgress');
    var current  = 0;
    var timer    = null;
    var paused   = false;

    /* Activate slide */
    function goTo(idx) {
        slides[current].classList.remove('hs--active');
        thumbs[current].classList.remove('hs__thumb--active');
        current = ((idx % TOTAL) + TOTAL) % TOTAL;
        slides[current].classList.add('hs--active');
        thumbs[current].classList.add('hs__thumb--active');
        /* Scroll thumb into view */
        /* Horizontal-only thumb scroll — scrollIntoView causes full page
           to jump back to hero section on every slide change. */
        var strip = document.getElementById('hsThumbs');
        if (strip) {
            var th     = thumbs[current];
            var tLeft  = th.offsetLeft;
            var tWidth = th.offsetWidth;
            var sLeft  = strip.scrollLeft;
            var sWidth = strip.offsetWidth;
            if (tLeft < sLeft) {
                strip.scrollLeft = tLeft;
            } else if (tLeft + tWidth > sLeft + sWidth) {
                strip.scrollLeft = tLeft + tWidth - sWidth;
            }
        }
        resetBar();
    }

    /* Progress bar */
    function resetBar() {
        if (!fill) return;
        fill.style.transition = 'none';
        fill.style.width = '0%';
        void fill.offsetWidth;
        fill.style.transition = 'width ' + DURATION + 'ms linear';
        fill.style.width = '100%';
    }

    /* Auto-play */
    function startTimer() {
        clearInterval(timer);
        timer = setInterval(function () {
            if (!paused) goTo(current + 1);
        }, DURATION);
    }

    function init() {
        if (!slides.length) return;

        resetBar();
        startTimer();

        /* Thumbnails */
        thumbs.forEach(function (th) {
            th.addEventListener('click', function () {
                goTo(parseInt(this.dataset.slide, 10));
                startTimer();
            });
        });

        /* Arrows */
        if (prevBtn) prevBtn.addEventListener('click', function () { goTo(current - 1); startTimer(); });
        if (nextBtn) nextBtn.addEventListener('click', function () { goTo(current + 1); startTimer(); });

        /* Touch */
        var track = document.querySelector('.hs__track');
        var tx = 0;
        if (track) {
            track.addEventListener('touchstart', function (e) { tx = e.changedTouches[0].clientX; }, { passive: true });
            track.addEventListener('touchend', function (e) {
                var d = tx - e.changedTouches[0].clientX;
                if (Math.abs(d) > 44) { goTo(d > 0 ? current + 1 : current - 1); startTimer(); }
            }, { passive: true });
        }

        /* Pause on hover */
        var section = document.querySelector('.hs');
        if (section) {
            section.addEventListener('mouseenter', function () {
                paused = true;
                if (fill) fill.style.transition = 'none';
            });
            section.addEventListener('mouseleave', function () {
                paused = false;
                resetBar();
                startTimer();
            });
        }

        /* Keyboard */
        document.addEventListener('keydown', function (e) {
            if (e.key === 'ArrowLeft')  { goTo(current - 1); startTimer(); }
            if (e.key === 'ArrowRight') { goTo(current + 1); startTimer(); }
        });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
}());
